import { IsNumber, IsOptional, IsString, MaxLength } from 'class-validator';

export class GetAddressesDto {
  @IsOptional()
  @IsString()
  @MaxLength(255)
  street?: string;

  @IsOptional()
  @IsString()
  @MaxLength(100)
  city?: string;

  @IsOptional()
  @IsString()
  @MaxLength(100)
  state?: string;

  @IsOptional()
  @IsString()
  @MaxLength(20)
  postalCode?: string;

  @IsOptional()
  latitude?: number;

  @IsOptional()
  longitude?: number;
}